# ── Cell 9: Isolation Forest
cells.append(md(
    "## 8. ML Anomaly Detection – Isolation Forest\n\n"
    "We use Isolation Forest to learn what 'normal' configuration changes look like "
    "and flag anomalous ones without hand-crafted rules."
))

cells.append(code(
    "from sklearn.ensemble import IsolationForest\n"
    "from sklearn.preprocessing import LabelEncoder\n"
    "\n"
    "# ── Feature engineering ─────────────────────────────────────\n"
    "df_ml = df.copy()\n"
    "\n"
    "le_dict = {}\n"
    "for col in ['change_type', 'severity', 'status', 'control_type']:\n"
    "    le_dict[col] = LabelEncoder()\n"
    "    df_ml[f'{col}_enc'] = le_dict[col].fit_transform(df_ml[col].astype(str))\n"
    "\n"
    "df_ml['is_off_hours_int']  = df_ml['is_off_hours'].astype(int)\n"
    "df_ml['is_regression_int'] = df_ml['is_regression'].astype(int)\n"
    "\n"
    "FEATURES = ['change_type_enc', 'severity_enc', 'status_enc', 'control_type_enc',\n"
    "            'is_off_hours_int', 'is_regression_int', 'change_hour', 'risk_score']\n"
    "\n"
    "X = df_ml[FEATURES].fillna(0).values\n"
    "\n"
    "print(f'Feature matrix: {X.shape}')\n"
    "print(f'Features: {FEATURES}')\n"
    "\n"
    "# ── Train Isolation Forest ───────────────────────────────────\n"
    "# contamination=0.40: expect ~40% anomalies (aligned with 57% label distribution)\n"
    "iso_forest = IsolationForest(\n"
    "    contamination=0.40,\n"
    "    n_estimators=200,\n"
    "    max_samples='auto',\n"
    "    random_state=42,\n"
    "    n_jobs=-1\n"
    ")\n"
    "\n"
    "iso_forest.fit(X)\n"
    "iso_pred   = iso_forest.predict(X)\n"
    "iso_scores = iso_forest.decision_function(X)\n"
    "\n"
    "df_ml['iso_anomaly'] = (iso_pred == -1).astype(int)\n"
    "df_ml['iso_score']   = iso_scores\n"
    "\n"
    "print(f'\\nIsolation Forest Results:')\n"
    "print(f'  Anomalies: {df_ml[\"iso_anomaly\"].sum()}')\n"
    "print(f'  Normal   : {(df_ml[\"iso_anomaly\"] == 0).sum()}')\n"
    "\n"
    "# Anomaly score distribution\n"
    "fig, ax = plt.subplots(figsize=(10, 4))\n"
    "fig.patch.set_facecolor('#0a0e1a')\n"
    "ax.set_facecolor('#1a1f2e')\n"
    "ax.hist(iso_scores[iso_pred == 1], bins=40, alpha=0.7,\n"
    "        color='#2ed573', label='Normal', density=True)\n"
    "ax.hist(iso_scores[iso_pred == -1], bins=40, alpha=0.7,\n"
    "        color='#ff4757', label='Anomaly', density=True)\n"
    "ax.axvline(0, color='#ffd32a', linestyle='--', linewidth=1.5, label='Decision boundary')\n"
    "ax.set_title('Isolation Forest Anomaly Score Distribution', color='white', fontweight='bold')\n"
    "ax.set_xlabel('Anomaly Score (lower = more anomalous)', color='#8892a4')\n"
    "ax.set_ylabel('Density', color='#8892a4')\n"
    "ax.tick_params(colors='#8892a4')\n"
    "ax.legend(facecolor='#1a1f2e', labelcolor='white')\n"
    "plt.tight_layout()\n"
    "plt.savefig('isolation_forest_scores.png', dpi=150, bbox_inches='tight', facecolor='#0a0e1a')\n"
    "plt.show()\n"
))

